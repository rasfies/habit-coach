// ---------------------------------------------------------------------------
// User avatar
// ---------------------------------------------------------------------------

interface UserAvatarProps {
  name: string;
  avatarUrl?: string | null;
}

function UserAvatar({ name, avatarUrl }: UserAvatarProps) {
  const initials = name
    .split(" ")
    .map((p) => p[0]?.toUpperCase() ?? "")
    .slice(0, 2)
    .join("");

  if (avatarUrl) {
    return (
      <img
        src={avatarUrl}
        alt={name}
        className="h-8 w-8 rounded-full object-cover ring-2 ring-brand-200"
      />
    );
  }

  return (
    <div className="flex h-8 w-8 items-center justify-center rounded-full bg-brand-100 text-xs font-semibold text-brand-700 ring-2 ring-brand-200">
      {initials}
    </div>
  );
}

